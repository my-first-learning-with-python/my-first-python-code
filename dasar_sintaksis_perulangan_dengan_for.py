""""
program perulangan membaca buku denga for
"""

jumlah_buku = 30
print(f"Jumlah buku yang inin dibaca adalah {jumlah_buku} buku")

jumlah_buku_yang_dibaca = 0
print(f"Jumlah buku yang sudah dibaca sebanyak {jumlah_buku_yang_dibaca} buku")

for jumlah_buku_yang_dibaca in range (1,jumlah_buku + 1):
    print(f"buku ke {jumlah_buku_yang_dibaca} telah dibaca")

print(jumlah_buku)